def crack(guess):
    v2 = 0
    for i in range(0, len(guess)):
        v4 = ord(guess[i]) ^ 0x33 & 0xFF
        v5 = v4 + 0x44 & 0xFF
        v6 = v5 ^ 0x55 & 0xFF
        v7 = v6 - 0x66 & 0xFF
        v8 = v7 ^ v2 & 0xFF
        if not v8 == key[i]:
            return False
        v2 += v8 
    return True
    
